     */
    public function store(Request $request)
    {
        $post = Order::create([
            'idkendaraan'   => $request->idkendaraan,
            'jumlah_penjualan'   => $request->jumlah_penjualan,
            'harga'   => $request->harga,
            'harga_total' => $request->harga * $request->jumlah_penjualan
            
        ]);
        
        return response()->json($post);
        //
    }
    
